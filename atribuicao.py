# Atribuições Exemplos

#Atribuição da soma de dois numeros inteiros
resultado = 2+3
print( "Resultado: ", resultado)

#Atribuição de um texto (String)
nome = "Pedro"
print(" Nome: ", nome)

#Saida F"String" com a variavel entre as chaves
nome2 = "Maria"
print( f"Olá, {nome2}. Tudo Bem?")

#somando dois ao valor anterior da variavel
resultado += 2
print( "Resultado: ", resultado)

#subtraindo dois ao valor anterior da variavel
resultado -= 2
print( "Resultado: ", resultado)

#jogando porcentagem ao valor anterior da variavel
resultado %= 2
print( "Resultado: ", resultado)